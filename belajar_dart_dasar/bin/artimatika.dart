void main() {
  var first = 10;
  var second = 5;

  print(first + second);
  print(first * second);
  print(first - second);
  print(first ~/ second);
  print(first % second);
  print(first / second);

  // oprator penugasan
  print(first += second);
  print(first *= second);
  print(first -= second);
  print(first ~/= second);
  print(first %= second);

  dynamic a = 10;
  print(a /= 5);
}
